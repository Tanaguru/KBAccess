package org.opens.kbaccess.command;

/**
 *
 * @author blebail
 */
public class NewPasswordCommand extends ChangePasswordCommand {

    private String token;
    
    public NewPasswordCommand() {
    }

    public NewPasswordCommand(String currentPassword, String newPassword, String confirmationPassword, Long idAccount, String token) {
        super(currentPassword, newPassword, confirmationPassword);
        this.token = token;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
